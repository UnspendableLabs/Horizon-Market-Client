#!/usr/bin/env node
/**
 * TEMPORARY workaround for four defects in the published @kontor/sdk-native@0.3.0-rc.5
 * tarball. The first two plus the fourth break the iOS native build; the third
 * breaks Android.
 *
 * 1. Missing `homepage` (iOS)
 *    KontorSdkNative.podspec does `s.homepage = package["homepage"]`, and
 *    CocoaPods treats a missing homepage as a hard validation error, so
 *    `pod install` (hence `expo run:ios`) aborts with:
 *      ERROR | attributes: Missing required attribute `homepage`.
 *
 * 2. Missing `codegenConfig` (iOS)
 *    @kontor/sdk-native is a uniffi-bindgen-react-native TurboModule. Its
 *    ios/KontorSdkNative.h imports the RN-codegen spec header under the New
 *    Architecture:
 *      #ifdef RCT_NEW_ARCH_ENABLED
 *      #import "RNNativeModuleSpec.h"          // defines <NativeKontorSdkNativeSpec>
 *    That header is generated by React Native's iOS codegen from the TS spec at
 *    src/generated/spec/NativeKontorSdkNative.ts — but ONLY when the package
 *    declares a `codegenConfig`. Without it codegen skips the library and the
 *    build fails with:
 *      'RNNativeModuleSpec.h' file not found
 *
 *    The values below are ubrn's documented defaults (name "RNNativeModuleSpec"
 *    matches the #import; jsSrcsDir "src" is scanned recursively for the
 *    Native*.ts spec) with the package's real Android namespace.
 *
 * 3. Stale duplicate Android codegen (Android)
 *    The tarball ships its own dirty `android/build/generated/source/codegen/jni/`
 *    dir, and it contains TWO generations of the TurboModule codegen: the current
 *    one (matching codegenConfig.name "RNNativeModuleSpec") plus a leftover from a
 *    previous module name ("KontorSdkNative"). The CMakeLists there globs `*.cpp`,
 *    so both `<name>-generated.cpp` files compile — and both define
 *    `facebook::react::NativeKontorSdkNativeSpecJSI(...)`. The app's C++ link then
 *    fails with:
 *      ld.lld: error: duplicate symbol:
 *        facebook::react::NativeKontorSdkNativeSpecJSI::NativeKontorSdkNativeSpecJSI(...)
 *    Only `<codegenConfig.name>-generated.cpp` is wired up (the app's autolinking.cpp
 *    calls `RNNativeModuleSpec_ModuleProvider`); the stale pair is dead weight, so we
 *    delete every `*-generated.cpp`/`*.h` in that dir whose prefix isn't the current
 *    codegen name.
 *
 * 4. Mis-named iOS xcframework (iOS)
 *    The tarball ships the prebuilt binary as `KontorSdkNativeFramework.xcframework`,
 *    but the `.framework` INSIDE it (per its Info.plist LibraryPath, its
 *    CFBundleExecutable, and its Mach-O install name `@rpath/KontorSdkNative.framework/
 *    KontorSdkNative`) is `KontorSdkNative.framework`. CocoaPods derives the
 *    `-framework <name>` linker flag AND the copy-target `.framework` name from the
 *    xcframework's *basename*, not from the Info.plist — so it emits
 *    `-framework KontorSdkNativeFramework` and tries to copy
 *    `KontorSdkNativeFramework.framework`, neither of which exists. The link then
 *    fails with:
 *      ld: framework 'KontorSdkNativeFramework' not found
 *    (and even if it linked, dyld would look for KontorSdkNative.framework at runtime
 *    per the install name and crash). CocoaPods requires the xcframework basename to
 *    match its internal framework, so we rename the bundle to
 *    `KontorSdkNative.xcframework` and repoint the podspec's `vendored_frameworks`.
 *
 * A native rebuild is required after this runs (`npx expo run:ios` /
 * `npx expo run:android`), and the podspec change needs the `pod install` that
 * `expo run:ios` performs.
 *
 * DELETE this script and its `postinstall` hook once @kontor/sdk-native ships a
 * package.json carrying `homepage` + `codegenConfig` and a clean android/build dir.
 */
const fs = require("fs");
const path = require("path");

const HOMEPAGE = "https://github.com/KontorProtocol/Kontor";

const CODEGEN_CONFIG = {
  name: "RNNativeModuleSpec",
  type: "modules",
  jsSrcsDir: "src",
  android: {
    javaPackageName: "com.kontor.sdknative",
  },
};

const OLD_XCFRAMEWORK = "KontorSdkNativeFramework.xcframework";
const NEW_XCFRAMEWORK = "KontorSdkNative.xcframework";

// Remove leftover `<oldName>-generated.cpp` / `<oldName>.h` from the shipped Android
// codegen dir so only the current codegen name survives the CMake `*.cpp` glob.
function pruneStaleAndroidCodegen(pkgPath, codegenName) {
  const jniDir = path.join(
    path.dirname(pkgPath),
    "android/build/generated/source/codegen/jni",
  );
  if (!fs.existsSync(jniDir)) return [];

  const removed = [];
  for (const entry of fs.readdirSync(jniDir)) {
    const m = entry.match(/^(.+)-generated\.cpp$/);
    if (!m || m[1] === codegenName) continue; // keep the current codegen's cpp
    const staleBase = m[1];
    for (const sibling of [`${staleBase}-generated.cpp`, `${staleBase}.h`]) {
      const p = path.join(jniDir, sibling);
      if (fs.existsSync(p)) {
        fs.rmSync(p);
        removed.push(sibling);
      }
    }
  }
  return removed;
}

// Rename the iOS xcframework bundle so its basename matches the KontorSdkNative.framework
// it wraps, and repoint the podspec at the new name. Idempotent: skips once renamed.
function fixIosXcframeworkName(pkgPath) {
  const pkgDir = path.dirname(pkgPath);
  const oldPath = path.join(pkgDir, OLD_XCFRAMEWORK);
  const newPath = path.join(pkgDir, NEW_XCFRAMEWORK);
  const podspecPath = path.join(pkgDir, "KontorSdkNative.podspec");
  const changes = [];

  if (fs.existsSync(oldPath) && !fs.existsSync(newPath)) {
    fs.renameSync(oldPath, newPath);
    changes.push("renamed xcframework → KontorSdkNative.xcframework");
  }

  if (fs.existsSync(podspecPath)) {
    const src = fs.readFileSync(podspecPath, "utf8");
    if (src.includes(OLD_XCFRAMEWORK)) {
      fs.writeFileSync(podspecPath, src.split(OLD_XCFRAMEWORK).join(NEW_XCFRAMEWORK));
      changes.push("repointed podspec vendored_frameworks");
    }
  }
  return changes;
}

try {
  const pkgPath = require.resolve("@kontor/sdk-native/package.json");
  const pkg = JSON.parse(fs.readFileSync(pkgPath, "utf8"));
  const fixed = [];

  if (!pkg.homepage) {
    pkg.homepage = HOMEPAGE;
    fixed.push("homepage");
  }
  if (!pkg.codegenConfig) {
    pkg.codegenConfig = CODEGEN_CONFIG;
    fixed.push("codegenConfig");
  }

  if (fixed.length) {
    fs.writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + "\n");
    console.log(
      `[fix-kontor-packaging] added ${fixed.join(" + ")} to @kontor/sdk-native — iOS pod validation + codegen can now succeed`,
    );
  }

  const codegenName = pkg.codegenConfig?.name || CODEGEN_CONFIG.name;
  const removed = pruneStaleAndroidCodegen(pkgPath, codegenName);
  if (removed.length) {
    console.log(
      `[fix-kontor-packaging] removed stale Android codegen (${removed.join(", ")}) — Android C++ link no longer sees a duplicate NativeKontorSdkNativeSpecJSI`,
    );
  }

  const xcfwChanges = fixIosXcframeworkName(pkgPath);
  if (xcfwChanges.length) {
    console.log(
      `[fix-kontor-packaging] ${xcfwChanges.join(" + ")} — CocoaPods now emits -framework KontorSdkNative to match the bundled binary`,
    );
  }
} catch (err) {
  // @kontor/sdk-native not installed yet, or already fixed upstream — nothing to do.
  console.warn("[fix-kontor-packaging] skipped:", err.message);
}
